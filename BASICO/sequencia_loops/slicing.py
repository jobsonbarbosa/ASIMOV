# Slicing ou fatiamento

pessoas = ['Jobson', 'Joelma', 'Carolina', 'Oliver', 'Maurina']

print(pessoas[1:3]) # o fatiamento não inclui o último elemento.

# uma logica para essa comportamento é que:
# se voce diminuior o primeiro elemento pelo último
# sempre sera a quantidade de elementos que ira retornar
# no exemplo 3-1 = 2
# resultando ['Joelma', 'Carolina']
